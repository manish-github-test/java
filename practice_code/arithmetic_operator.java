public class arithmetic_operator {
  public static void main(String[] args) {
    
      int a = 5;
      int b = -6;
      System.out.println(a+b);
      System.out.println(a-b);
      System.out.println(a*b);
      System.out.println(a/b);
      System.out.println(a%b);
      System.out.println(10<<2); //10*(2^2)
      System.out.println(10<<3); //10*(2^3)
      System.out.println(10>>3); //10/(2^3)
      System.out.println(10>>2); //10/(2^2)
}  
}
